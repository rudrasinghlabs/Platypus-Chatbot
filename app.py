MAX_SOURCE_CARDS = 4
MAX_SEARCH_RESULTS = 8


def _doc_choices() -> List[Tuple[str, str]]:
    return [(d.filename, d.doc_id) for d in app_state["documents"].values()]


def _chip_updates_and_prompts(doc_id: Optional[str]):
    filename = app_state["documents"][doc_id].filename if doc_id in app_state.get("documents", {}) else None
    chips = get_dynamic_chips(filename, max_chips=MAX_CHIPS)
    labels = [c[0] for c in chips]
    prompts = [c[1] for c in chips]
    updates = [
        gr.update(value=labels[i], visible=True) if i < len(labels) else gr.update(visible=False)
        for i in range(MAX_CHIPS)
    ]
    return updates, prompts


def handle_upload(files) -> Generator[Tuple, None, None]:
    """Index one or more uploaded PDFs, yielding staged status updates."""
    if not files:
        chip_updates, chip_prompts = _chip_updates_and_prompts(app_state.get("active_filter_doc_id"))
        yield ("⚠️ No PDF uploaded. Please choose at least one file.",
               render_document_list_html(), render_stats_html(), gr.update(), gr.update(), *chip_updates, chip_prompts)
        return

    file_paths = [f if isinstance(f, str) else f.name for f in files]
    total = len(file_paths)
    chip_updates, chip_prompts = _chip_updates_and_prompts(app_state.get("active_filter_doc_id"))

    for i, path in enumerate(file_paths, start=1):
        filename = os.path.basename(path)
        stage_prefix = f"({i}/{total}) {filename}: "

        for stage in ["📄 Reading document...", "✂️ Chunking pages...",
                      "🧠 Creating embeddings...", "📚 Building knowledge base..."]:
            yield (stage_prefix + stage, render_document_list_html(), render_stats_html(),
                   gr.update(), gr.update(), *chip_updates, chip_prompts)

        try:
            index_pdf(path, filename)
            status = f'<span class="p-success-flash">✅ {filename} indexed successfully.</span>'
        except ValueError as exc:
            status = f"❌ {filename}: {exc}"
        except Exception as exc:  # noqa: BLE001
            status = f"❌ {filename}: unexpected error — {exc}"

        yield (status, render_document_list_html(), render_stats_html(),
               gr.update(), gr.update(), *chip_updates, chip_prompts)

    doc_choices = _doc_choices()
    if not app_state.get("active_filter_doc_id") and doc_choices:
        app_state["active_filter_doc_id"] = doc_choices[0][1]
    chip_updates, chip_prompts = _chip_updates_and_prompts(app_state.get("active_filter_doc_id"))

    yield (
        f"✅ Indexing complete. {len(app_state['documents'])} document(s) ready.",
        render_document_list_html(),
        render_stats_html(),
        gr.update(choices=doc_choices, value=(doc_choices[0][1] if doc_choices else None)),
        gr.update(choices=doc_choices, value=app_state.get("active_filter_doc_id")),
        *chip_updates, chip_prompts,
    )


def handle_remove(doc_id: Optional[str]):
    if doc_id:
        remove_document(doc_id)
    if app_state.get("active_filter_doc_id") not in app_state["documents"]:
        remaining = _doc_choices()
        app_state["active_filter_doc_id"] = remaining[0][1] if remaining else None
    doc_choices = _doc_choices()
    chip_updates, chip_prompts = _chip_updates_and_prompts(app_state.get("active_filter_doc_id"))
    return (
        render_document_list_html(),
        render_stats_html(),
        gr.update(choices=doc_choices, value=(doc_choices[0][1] if doc_choices else None)),
        gr.update(choices=doc_choices, value=(doc_choices[0][1] if doc_choices else None)),
        gr.update(choices=doc_choices, value=app_state.get("active_filter_doc_id")),
        *chip_updates, chip_prompts,
    )


def handle_clear_session():
    clear_session()
    app_state["search_scope"] = "library"
    app_state["active_filter_doc_id"] = None
    app_state["conversation_log"] = []
    app_state["last_search_results"] = []
    chip_updates, chip_prompts = _chip_updates_and_prompts(None)
    return (
        [],  # chat history
        render_document_list_html(),
        render_stats_html(),
        gr.update(choices=[], value=None),
        gr.update(choices=[], value=None),
        gr.update(choices=[], value=None),
        None,  # preview image
        *[gr.update(visible=False, value="") for _ in range(MAX_SOURCE_CARDS)],
        *chip_updates, chip_prompts,
        *[gr.update(visible=False, value="") for _ in range(MAX_SEARCH_RESULTS)],
    )


def _empty_source_updates():
    return [gr.update(visible=False, value="") for _ in range(MAX_SOURCE_CARDS)]


def _source_updates(sources: List[Dict]):
    updates = []
    for i in range(MAX_SOURCE_CARDS):
        if i < len(sources):
            updates.append(gr.update(visible=True, value=source_card_label(sources[i])))
        else:
            updates.append(gr.update(visible=False, value=""))
    return updates


def handle_ask(question: str, history):
    """Stream an answer into the chatbot, yielding (history, textbox, status, *source_buttons)."""
    history = history or []

    if not question or not question.strip():
        yield history, question, "", *_empty_source_updates()
        return

    app_state["last_question"] = question
    history = history + [{"role": "user", "content": question}, {"role": "assistant", "content": ""}]

    doc_filter = app_state.get("active_filter_doc_id") if app_state.get("search_scope") == "single" else None

    for update in answer_pipeline(question, doc_filter=doc_filter):
        stage = update["stage"]
        status_html = (
            f'<div class="p-loading-row"><span class="p-loading-dot"></span>{html.escape(stage)}</div>'
            if stage not in ("idle", "done") else ""
        )

        if update["partial_answer"]:
            history[-1]["content"] = format_assistant_message(
                update.get("summary", ""), update["partial_answer"], update.get("confidence"),
                streaming=not update["done"],
            )

        source_updates = _source_updates(update["sources"]) if update["sources"] else _empty_source_updates()
        app_state["last_sources"] = update["sources"]

        yield history, ("" if update["done"] else question), status_html, *source_updates


def handle_regenerate(history):
    question = app_state.get("last_question")
    if not question:
        yield history, *_empty_source_updates()
        return
    # Drop the previous assistant turn (and the user turn we're about to re-add) before regenerating.
    trimmed = history[:-2] if len(history) >= 2 else []
    if app_state.get("conversation_log"):
        app_state["conversation_log"].pop()  # remove the stale logged turn we're about to redo
    for h, _, _, *sources in handle_ask(question, trimmed):
        yield h, *sources


def handle_clear_chat():
    app_state["last_question"] = None
    app_state["last_sources"] = []
    app_state["conversation_log"] = []
    return [], "", *_empty_source_updates()


def handle_chip_click(index: int, chip_prompts: List[str], history):
    if not chip_prompts or index >= len(chip_prompts):
        yield history, "", "", *_empty_source_updates()
        return
    prompt_text = chip_prompts[index]
    for h, q, s, *sources in handle_ask(prompt_text, history):
        yield h, q, s, *sources


def handle_source_click(index: int):
    sources = app_state.get("last_sources", [])
    if index >= len(sources):
        return gr.update(), gr.update(), None
    source = sources[index]
    image = render_pdf_page(source["doc_id"], source["page"], highlight_snippet=source["text"])
    return source["doc_id"], source["page"], image


def handle_preview_nav(doc_id: Optional[str], page_number: Optional[int]):
    if not doc_id or not page_number:
        return None
    image = render_pdf_page(doc_id, int(page_number))
    return image


def handle_doc_select(doc_id: Optional[str]):
    if not doc_id:
        return 1, None
    image = render_pdf_page(doc_id, 1)
    return 1, image


def handle_scope_change(scope_label: str):
    app_state["search_scope"] = "single" if scope_label == "Selected Document" else "library"
    return render_document_list_html(), render_stats_html()


def handle_active_filter_change(doc_id: Optional[str]):
    app_state["active_filter_doc_id"] = doc_id
    chip_updates, chip_prompts = _chip_updates_and_prompts(doc_id)
    return (render_document_list_html(), render_stats_html(), *chip_updates, chip_prompts)


def _empty_search_result_updates():
    return [gr.update(visible=False, value="") for _ in range(MAX_SEARCH_RESULTS)]


def handle_search_pdf(query: str, scope_label: str):
    if not query or not query.strip():
        return _empty_search_result_updates()
    doc_id = app_state.get("active_filter_doc_id") if scope_label == "Selected Document" else None
    results = search_in_pdf(query, doc_id=doc_id, max_results=MAX_SEARCH_RESULTS)
    app_state["last_search_query"] = query
    app_state["last_search_results"] = results
    updates = []
    for i in range(MAX_SEARCH_RESULTS):
        if i < len(results):
            r = results[i]
            label = f"📄 {r['filename']} · p.{r['page']} · {r['count']} match(es)"
            updates.append(gr.update(visible=True, value=label))
        else:
            updates.append(gr.update(visible=False, value=""))
    return updates


def handle_search_result_click(index: int):
    results = app_state.get("last_search_results", [])
    if index >= len(results):
        return gr.update(), gr.update(), None
    r = results[index]
    image = render_pdf_page(r["doc_id"], r["page"], highlight_snippet=app_state.get("last_search_query", ""))
    return r["doc_id"], r["page"], image


def handle_export_markdown():
    path = export_conversation_markdown()
    if not path:
        gr.Warning("Nothing to export yet — ask a question first.")
        return gr.update(value=None, visible=False)
    return gr.update(value=path, visible=True)


def handle_export_pdf():
    path = export_conversation_pdf()
    if not path:
        gr.Warning("Nothing to export yet — ask a question first.")
        return gr.update(value=None, visible=False)
    return gr.update(value=path, visible=True)

