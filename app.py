def export_conversation_pdf() -> Optional[str]:
    log = app_state.get("conversation_log", [])
    if not log:
        return None

    doc = fitz.open()
    page_w, page_h = fitz.paper_size("a4")
    margin = 48
    line_h = 14
    y = margin
    page = doc.new_page(width=page_w, height=page_h)

    def new_page():
        nonlocal page, y
        page = doc.new_page(width=page_w, height=page_h)
        y = margin

    def write_line(text: str, size: float = 10.5, color=(0.07, 0.07, 0.1), bold: bool = False):
        nonlocal y
        wrapped = textwrap.wrap(text, width=95) or [""]
        for line in wrapped:
            if y > page_h - margin:
                new_page()
            fontname = "helv" if not bold else "hebo"
            page.insert_text((margin, y), line, fontsize=size, fontname=fontname, color=color)
            y += line_h

    write_line(f"{PLATYPUS_NAME} — Conversation Export", size=18, bold=True, color=(0.36, 0.37, 0.93))
    write_line(f"Exported {datetime.now().strftime('%Y-%m-%d %H:%M')}", size=9, color=(0.4, 0.4, 0.5))
    y += 8

    for turn in log:
        write_line(f"[{turn['timestamp']}]", size=9, color=(0.4, 0.4, 0.5))
        write_line(f"Q: {turn['question']}", bold=True)
        write_line(f"A: {turn['answer']}")
        if turn.get("confidence"):
            write_line(f"Confidence: {turn['confidence']}", size=9, color=(0.4, 0.4, 0.5))
        for s in turn.get("sources", []):
            write_line(f"  Source: {s['filename']} (p.{s['page']}, {round(s['similarity']*100)}% match)",
                       size=9, color=(0.4, 0.4, 0.5))
        y += 10

    path = os.path.join("/tmp", f"platypus_export_{uuid.uuid4().hex[:8]}.pdf")
    doc.save(path)
    doc.close()
    return path

