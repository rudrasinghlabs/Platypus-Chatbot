# Smart prompt chips — a curated pool of document-analysis prompts. These are
# generic-but-useful across most PDFs; swap `get_dynamic_chips` for an LLM call
# (e.g. "read the first page and propose 6 chip prompts") if you want chips that
# are genuinely tailored to each document's content rather than its presence.
# ---------------------------------------------------------------------------
CHIP_POOL = [
    ("📝 Summarize", "Summarize this document in a few clear paragraphs."),
    ("💡 Explain", "Explain the main ideas of this document in simple terms."),
    ("❓ Create Quiz", "Create a short 5-question quiz based on this document, with answers."),
    ("📖 Find Definitions", "Find and explain the key terms and definitions in this document."),
    ("📅 Extract Dates", "List all dates, deadlines, or events mentioned in this document."),
    ("🗂️ List Key Topics", "List the main topics covered in this document, as bullet points."),
    ("🎤 Interview Questions", "Generate 5 interview questions someone could be asked based on this document."),
    ("🧠 Explain Difficult Concepts", "Identify and explain the most difficult concepts in this document, simply."),
    ("⚖️ Compare Sections", "Compare and contrast the main sections or parts of this document."),
    ("📓 Revision Notes", "Turn this document into concise revision notes."),
]

MAX_CHIPS = 6


def get_dynamic_chips(doc_filename: Optional[str] = None, max_chips: int = MAX_CHIPS) -> List[Tuple[str, str]]:
    """Pick a stable, useful subset of chips. Rotates slightly by filename so different
    documents don't always show the exact same six prompts."""
    if not doc_filename:
        return CHIP_POOL[:max_chips]
    offset = sum(ord(c) for c in doc_filename) % len(CHIP_POOL)
    rotated = CHIP_POOL[offset:] + CHIP_POOL[:offset]
    # Always keep Summarize first — it's the most useful default entry point.
    rotated = sorted(rotated, key=lambda c: c != CHIP_POOL[0])
    return rotated[:max_chips]


