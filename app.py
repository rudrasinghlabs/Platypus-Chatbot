"""## Retrieval

Same top-k similarity search as the original (`top_k=4`), now spanning every indexed document and
returning `filename` / `doc_id` / `page` alongside each chunk, plus a similarity score used for the
confidence badge.
"""

def retrieve_top_chunks(question: str, top_k: int = 4, doc_id: Optional[str] = None) -> List[Dict]:
    """Embed the question and retrieve the top-k most similar chunks.

    If `doc_id` is given, retrieval is scoped ONLY to chunks belonging to that document
    (this is the fix for the "selecting Profile.pdf still pulls from Session1.pdf" bug —
    previously every query searched the whole collection regardless of selection).
    Pass doc_id=None to search the entire library.
    """
    collection = get_collection()
    if collection.count() == 0:
        return []

    where = {"doc_id": doc_id} if doc_id else None

    # When filtering to one document, ask Chroma how many chunks that document actually has,
    # so n_results never exceeds it (Chroma errors if n_results > matching rows).
    if where:
        try:
            available = len(collection.get(where=where, include=[])["ids"])
        except Exception:
            available = top_k
        n_results = max(1, min(top_k, available))
        if available == 0:
            return []
    else:
        n_results = min(top_k, collection.count())

    question_embedding = embed_texts([question])[0]
    results = collection.query(
        query_embeddings=[question_embedding], n_results=n_results, where=where,
    )

    documents = results.get("documents", [[]])[0]
    metadatas = results.get("metadatas", [[]])[0]
    distances = results.get("distances", [[]])[0]

    retrieved = []
    for doc, meta, dist in zip(documents, metadatas, distances):
        similarity = max(0.0, min(1.0, 1.0 - dist))  # cosine space -> similarity
        retrieved.append({
            "text": doc,
            "page": meta.get("page"),
            "filename": meta.get("filename"),
            "doc_id": meta.get("doc_id"),
            "similarity": similarity,
        })
    return retrieved


def confidence_from_chunks(retrieved: List[Dict]) -> str:
    """Heuristic confidence label from the best retrieval similarity score.

    This is a simple, transparent heuristic (not a calibrated probability): it reflects how close the
    single best-matching chunk is to the question in embedding space, thresholds chosen for typical
    all-MiniLM-L6-v2 cosine similarity ranges on prose text.
    """
    if not retrieved:
        return "low"
    best = max(c["similarity"] for c in retrieved)
    if best >= 0.55:
        return "high"
    if best >= 0.35:
        return "medium"
    return "low"


def build_contextual_query(question: str, conversation_log: List[Dict], max_turns: int = 2) -> str:
    """Fold the last few turns into the retrieval query so pronouns like "it" or "that page"
    resolve to something embeddable, instead of searching for the bare word "it"."""
    if not conversation_log:
        return question
    recent = conversation_log[-max_turns:]
    context_bits = [f"{t['question']} {t['answer'][:160]}" for t in recent]
    return " ".join(context_bits) + " " + question

"""## Prompt Builder

Same grounding rules as the original prompt (answer only from context, never fabricate, be explicit when
the answer isn't in the document). The one addition is asking for a `Summary:` / `Answer:` structure so the
UI can show a short summary above the full answer — sources, page numbers, and confidence are computed
programmatically from retrieval metadata (not left to the model), so citations can never be hallucinated.
"""

PROMPT_TEMPLATE = """You are a helpful assistant answering questions about one or more documents.

{conversation_section}Answer ONLY using the context below. Never make up information.

Respond in exactly this structure:
Summary: <one short sentence summarizing the answer>
Answer: <the full, detailed answer>

If the answer is not contained in the context, set Summary to "Not found in the documents." and explain \
that honestly in Answer instead of guessing.

Context:
{retrieved_chunks}

Question:
{user_question}

Response:"""


def build_conversation_section(conversation_log: List[Dict], max_turns: int = 2) -> str:
    """Short recap of the last turns so the model can resolve references like "it" or "that page"."""
    if not conversation_log:
        return ""
    recent = conversation_log[-max_turns:]
    lines = ["Conversation so far (use this only to resolve references like \"it\" or \"that\"; "
             "the current question's own answer must still come from the Context below):"]
    for t in recent:
        lines.append(f"- Q: {t['question']}\n  A: {t['answer'][:200]}")
    return "\n".join(lines) + "\n\n"


def build_prompt(question: str, retrieved_chunks: List[Dict], conversation_log: Optional[List[Dict]] = None) -> str:
    """Build the grounded RAG prompt from retrieved chunks, the question, and recent conversation memory."""
    context_blocks = [
        f"[{chunk['filename']} — Page {chunk['page']}] {chunk['text']}" for chunk in retrieved_chunks
    ]
    context = "\n\n".join(context_blocks)
    conversation_section = build_conversation_section(conversation_log or [])
    return PROMPT_TEMPLATE.format(
        retrieved_chunks=context, user_question=question, conversation_section=conversation_section
    )


def parse_summary_answer(raw_text: str) -> Tuple[str, str]:
    """Parse the model's 'Summary: ... Answer: ...' structure; fall back gracefully if it didn't comply."""
    match = re.search(r"Summary:\s*(.*?)\n+Answer:\s*(.*)", raw_text, re.DOTALL)
    if match:
        summary = match.group(1).strip()
        answer = match.group(2).strip()
        return summary, answer
    return "", raw_text.strip()

"""## Streaming Generation

The original used a single blocking `pipeline()` call. To make the UI feel alive (Feature 5), this adds a
token-by-token generator using `TextIteratorStreamer`, running `model.generate` on a background thread —
the standard Hugging Face pattern for streaming. Same model, same greedy/low-temperature decoding settings
as the original (`do_sample=False`, `temperature=0.1`, `max_new_tokens=256`).
"""

NOT_FOUND_MESSAGE = "I couldn't find that information in the uploaded document(s)."


def stream_llm_response(prompt: str) -> Generator[str, None, None]:
    """Yield the growing generated text for `prompt`, token by token."""
    model, tokenizer = get_llm()
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
    streamer = TextIteratorStreamer(tokenizer, skip_prompt=True, skip_special_tokens=True)

    generation_kwargs = dict(
        **inputs,
        streamer=streamer,
        max_new_tokens=256,
        do_sample=False,
        temperature=0.1,
        pad_token_id=tokenizer.eos_token_id,
    )

    thread = threading.Thread(target=model.generate, kwargs=generation_kwargs)
    thread.start()

    partial = ""
    for token_text in streamer:
        partial += token_text
        yield partial
    thread.join()

"""## Answer Pipeline

Orchestrates retrieval → prompt → streaming generation, yielding staged status updates (Feature 6) so the
UI can show "Searching documents...", "Thinking...", "Writing answer..." etc. Every yielded dict has the
same shape so the Gradio callback can consume it uniformly.
"""

def answer_pipeline(question: str, doc_filter: Optional[str] = None) -> Generator[Dict, None, None]:
    """Generator yielding {'stage', 'partial_answer', 'summary', 'sources', 'confidence', 'done'} dicts.

    `doc_filter` is the doc_id to restrict retrieval to. Pass None to search the entire library.
    Conversation memory (recent Q/A turns from app_state["conversation_log"]) is folded into both
    the retrieval query and the prompt, so follow-ups like "explain it more simply" work.
    """
    base = {"partial_answer": "", "summary": "", "sources": [], "confidence": None, "done": False}

    if not question or not question.strip():
        yield {**base, "stage": "idle", "partial_answer": "Please enter a question.", "done": True}
        return

    if len(question.split()) > 200:
        msg = "Your question is quite long. Please shorten it to under 200 words so it can be processed effectively."
        yield {**base, "stage": "idle", "partial_answer": msg, "done": True}
        return

    if not app_state["documents"]:
        msg = "⚠️ Please upload and index at least one PDF before asking a question."
        yield {**base, "stage": "idle", "partial_answer": msg, "done": True}
        return

    if doc_filter and doc_filter not in app_state["documents"]:
        doc_filter = None  # selected document was removed since the filter was set

    conversation_log = app_state.get("conversation_log", [])

    yield {**base, "stage": "🔍 Searching documents..."}
    contextual_query = build_contextual_query(question, conversation_log)
    retrieved = retrieve_top_chunks(contextual_query, top_k=4, doc_id=doc_filter)

    if not retrieved:
        msg = NOT_FOUND_MESSAGE
        if doc_filter:
            filename = app_state["documents"][doc_filter].filename
            msg = f"I couldn't find that information in **{filename}**. Try switching search scope to Entire Library."
        yield {**base, "stage": "idle", "partial_answer": msg, "confidence": "low", "done": True}
        return

    confidence = confidence_from_chunks(retrieved)

    yield {**base, "stage": "🤖 Thinking...", "sources": retrieved, "confidence": confidence}

    prompt = build_prompt(question, retrieved, conversation_log)

    partial_raw = ""
    for partial_raw in stream_llm_response(prompt):
        summary, answer = parse_summary_answer(partial_raw)
        yield {
            "stage": "✨ Writing answer...",
            "partial_answer": answer if answer else partial_raw,
            "summary": summary,
            "sources": retrieved,
            "confidence": confidence,
            "done": False,
        }

    summary, answer = parse_summary_answer(partial_raw)
    if not answer.strip():
        answer = NOT_FOUND_MESSAGE
        confidence = "low"

    app_state["conversation_log"].append({
        "question": question,
        "answer": answer,
        "sources": retrieved,
        "confidence": confidence,
        "timestamp": datetime.now().strftime("%H:%M:%S"),
    })

    yield {
        "stage": "done",
        "partial_answer": answer,
        "summary": summary,
        "sources": retrieved,
        "confidence": confidence,
        "done": True,
    }

"""## PDF Preview Rendering

New: renders a single PDF page to an image for the right-hand preview panel. When a citation is clicked,
the matching snippet is located on the page with `page.search_for(...)` and highlighted before rendering —
with a fallback that shortens the search phrase if the full chunk doesn't match verbatim (chunks can span
line breaks or minor extraction artifacts).
"""

def _find_highlight_rects(page: "fitz.Page", snippet: str) -> List["fitz.Rect"]:
    words = snippet.split()
    for n in (14, 10, 6, 3):
        phrase = " ".join(words[:n])
        if not phrase:
            continue
        rects = page.search_for(phrase)
        if rects:
            return rects
    return []


def render_pdf_page(doc_id: str, page_number: int, highlight_snippet: Optional[str] = None, zoom: float = 1.7):
    """Return a PIL Image of the given 1-indexed page, with an optional highlighted snippet.

    Note: this returns a flattened raster image (Gradio's Image component), so the highlight can't
    fade with a CSS transition the way a DOM overlay could — it's baked into the rendered pixels
    each time you click a citation or search result, which is what "real" highlighting means here.
    """
    doc_info = app_state["documents"].get(doc_id)
    if not doc_info:
        return None

    pdf_doc = fitz.open(doc_info.path)
    try:
        idx = max(0, min(page_number - 1, len(pdf_doc) - 1))
        page = pdf_doc[idx]

        if highlight_snippet:
            for rect in _find_highlight_rects(page, highlight_snippet):
                annot = page.add_highlight_annot(rect)
                annot.set_colors(stroke=(0.99, 0.85, 0.35))
                annot.update()

        mat = fitz.Matrix(zoom, zoom)
        pix = page.get_pixmap(matrix=mat)
        image = Image.frombytes("RGB", [pix.width, pix.height], pix.samples)
        return image
    finally:
        pdf_doc.close()


def search_in_pdf(query: str, doc_id: Optional[str] = None, max_results: int = 8) -> List[Dict]:
    """Full-text search across page images (exact substring, via PyMuPDF's built-in search_for).

    doc_id=None searches every uploaded document ("Entire Library"); otherwise only that one
    ("Current Document"). Returns one entry per matching page, most-matches first.
    """
    results: List[Dict] = []
    if not query or not query.strip():
        return results

    docs = (
        [app_state["documents"][doc_id]] if doc_id and doc_id in app_state["documents"]
        else list(app_state["documents"].values())
    )

    for doc_info in docs:
        try:
            pdf_doc = fitz.open(doc_info.path)
        except Exception:
            continue
        try:
            for i, page in enumerate(pdf_doc, start=1):
                rects = page.search_for(query)
                if rects:
                    results.append({
                        "doc_id": doc_info.doc_id,
                        "filename": doc_info.filename,
                        "page": i,
                        "count": len(rects),
                    })
        finally:
            pdf_doc.close()

    results.sort(key=lambda r: r["count"], reverse=True)
    return results[:max_results]

