"""## PDF Extraction

Unchanged from the original: `PyPDF2`-based page-by-page text extraction, skipping empty pages, raising a
clear `ValueError` on corrupted or image-only PDFs.
"""

def extract_pdf_pages(pdf_path: str) -> List[Tuple[int, str]]:
    """Extract text from every page of a PDF.

    Returns a list of (page_number, page_text) tuples, 1-indexed. Raises ValueError
    if the PDF is corrupted, unreadable, or contains no extractable text at all.
    """
    if not pdf_path or not os.path.exists(pdf_path):
        raise ValueError("No PDF file was provided or the file could not be found.")

    try:
        reader = PdfReader(pdf_path)
    except PdfReadError as exc:
        raise ValueError(f"The PDF file appears to be corrupted: {exc}") from exc
    except Exception as exc:  # noqa: BLE001
        raise ValueError(f"Could not read the PDF file: {exc}") from exc

    if len(reader.pages) == 0:
        raise ValueError("The PDF file has no pages.")

    pages: List[Tuple[int, str]] = []
    for i, page in enumerate(reader.pages, start=1):
        try:
            text = page.extract_text() or ""
        except Exception:  # noqa: BLE001
            text = ""
        text = text.strip()
        if text:
            pages.append((i, text))

    if not pages:
        raise ValueError(
            "No extractable text was found in this PDF. It may be a scanned "
            "image-only document without an OCR text layer."
        )

    return pages

